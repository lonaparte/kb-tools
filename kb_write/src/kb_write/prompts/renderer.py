def load_fragment(name: str) -> str:
    """Read one fragment from the package's fragments/ directory."""
    pkg = resources.files("kb_write.prompts.fragments")
    return (pkg / name).read_text(encoding="utf-8")


def render_target(target: Target) -> str:
    """Compose a Target into its final Markdown string."""
    parts: list[str] = []
    parts.append(target.header)
    parts.append("")
    parts.append(GEN_BEGIN)
    parts.append("")
    parts.append(target.intro)
    parts.append("")
    for frag_name in target.fragments:
        frag = load_fragment(frag_name).strip()
        # Strip the "<!-- fragment: name -->" header comment for
        # rendered output cleanliness.
        lines = frag.splitlines()
        if lines and lines[0].startswith("<!-- fragment:"):
            lines = lines[1:]
        parts.append("\n".join(lines).strip())
        parts.append("")
    if target.target_note:
        parts.append("---")
        parts.append("")
        parts.append(target.target_note)
        parts.append("")
    parts.append(GEN_END)
    parts.append("")
    return "\n".join(parts)


def render_all() -> dict[str, str]:
    """Render every target; returns {filename: content}."""
    return {t.filename: render_target(t) for t in TARGETS}


def has_generated_block(content: str) -> bool:
    """Detect whether a file was produced by this renderer."""
    return GEN_BEGIN in content and GEN_END in content


def preserve_user_suffix(existing: str, new_generated: str) -> str:
    """When refreshing an existing file, keep anything the user appended
    AFTER the GEN_END marker.

    If `existing` has a GEN_END, the suffix is the text after it.
    If `existing` was hand-written (no markers), the whole file is
    treated as user content and appended after the new generated
    block — with a separator to make the transition obvious.
    """
    if GEN_END in existing:
        suffix = existing.split(GEN_END, 1)[1]
        # Trim a leading blank line to avoid piling up.
        return new_generated + suffix.lstrip("\n")
    # Legacy / hand-written file: preserve it as user-custom.
    if existing.strip():
        return (
            new_generated
            + "\n<!-- user-custom content below (kept from prior version) -->\n\n"
            + existing
        )
    return new_generated
