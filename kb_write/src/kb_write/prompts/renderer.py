"""Compose agent-facing prompt files from reusable fragments.

Design: fragments are the single source of truth. Targets are thin
declarative recipes — which fragments, in what order, under what
header. Adding a new target (e.g. Cursor, Aider) is ≤ 10 lines.

Each rendered output is wrapped with two HTML-comment markers so
`kb-write doctor` can detect drift (user hand-edited the generated
file) and `kb-write init --refresh` can regenerate without clobbering
any user-custom section appended AFTER the end marker.
"""
from __future__ import annotations

from dataclasses import dataclass
from importlib import resources
from typing import Sequence


# Markers wrapping the generated portion of a rendered prompt file.
# Anything BETWEEN these markers is managed by kb-write and will be
# overwritten by `init --refresh`. Anything outside them (e.g. after
# the end marker) is preserved across refreshes, letting users add
# their own custom instructions without losing them.
GEN_BEGIN = "<!-- kb-write generated: DO NOT EDIT inside this block -->"
GEN_END = "<!-- kb-write generated end -->"


@dataclass
class Target:
    """A declarative recipe for rendering one agent-facing prompt file."""
    filename: str                      # relative to kb_root, e.g. "CLAUDE.md"
    header: str                        # markdown heading for the file
    intro: str                         # 1-3 sentence intro above fragments
    fragments: Sequence[str]           # ordered list of fragment filenames (no path)
    target_note: str = ""              # optional footer explaining who reads it


